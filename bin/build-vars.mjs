import exportCustomVariables from 'postcss-export-custom-variables';
import postcssImport from 'postcss-import';
import postcss from 'postcss';
import fs from 'node:fs/promises';
import globby from 'globby';

const CSS_REGEXP = /\.css$/;

function cssVarsExporter(variables, options) {
    const contents = Object.entries(variables)
        .map(([name, value]) => {
            // regexp source https://github.com/csstools/postcss-export-custom-variables/blob/ce15caa33e52cca56b0b95cebfc9f32f3a8e6923/index.js#L112
            const valueString = JSON.stringify(value).replace(/(^|{|,)"(.+?)":/g, '$1$2:');

            return `export const ${name} = ${valueString};`;
        })
        .join('\n');

    return fs.writeFile(
        options.destination,
        `// THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n\n${contents}\n`,
        { encoding: 'utf8' },
    );
}

async function buildCssIndex() {
    const matches = await globby('src/*index.css');

    return Promise.all(
        matches.map(async (cssPath) => {
            const css = await fs.readFile(cssPath, { encoding: 'utf8' });

            return postcss()
                .use(postcssImport({}))
                .use(
                    exportCustomVariables({
                        exporter: cssVarsExporter,
                        destination: cssPath.replace(CSS_REGEXP, `.ts`),
                    }),
                )
                .process(css, { from: cssPath });
        }),
    );
}

async function buildPalettes() {
    const matches = await globby('src/@(colors|shadows)*.css');

    return Promise.all(
        matches.map(async (cssPath) => {
            const css = await fs.readFile(cssPath, { encoding: 'utf8' });
            const result = await postcss()
                .use(
                    exportCustomVariables({
                        exporter: cssVarsExporter,
                        destination: cssPath.replace(CSS_REGEXP, '.module.ts'),
                    }),
                )
                .process(css, { from: cssPath });

            return fs.writeFile(
                cssPath.replace(CSS_REGEXP, '.ts'),
                `// THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n\nexport default \`${result.css}\`;\n`,
                { encoding: 'utf8' },
            );
        }),
    );
}

async function main() {
    await Promise.all([buildCssIndex(), buildPalettes()]);
}

await main();
